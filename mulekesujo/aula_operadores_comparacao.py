"""
Operadores de Comperação:

== ➡️ Igual a
!= ➡️ Diferente de
>  ➡️ Maior que
<  ➡️ Menor que
>= ➡️ Maior ou Igual a
<= ➡️ Menor ou Igual a

Usaremos esses operadores sempre que precisarmos fazer comparações entre valores"""

# Igual a ==

print(10 == 10)
print(10 == 10.0)
print(10 == "10")
print(10 == 250 / 50 * 2)
print("senha456" == "Senha456")

# Diferente !=

print(10 != 10)
print(10 != 10.0)
print(10 != "10")
print(10 != 250 / 50 * 2)
print("senha456" != "Senha456")

# Maior que >

print(10 > 2)
print(10 > 50)
print(10 > 1000/900)
print(len("senha123") > 8)

# Menor que <

print(10 < 2)
print(10 < 50)
print(10 < 1000/900)
print(len("senha123") < 8)
print(len("senha12") < 8)

# Maior ou Igual a >=

print(10 >= 2)
print(10 >= 10)
print(10 >= 100)
print(len("senha12345") >= 10)
print(len("senha1234") >= 10)

# Menor ou Igual a >=

print(10 <= 2)
print(10 <= 10)
print(10 <= 100)
print(len("senha123456") <= 10)
print(len("senha12345") <= 10)