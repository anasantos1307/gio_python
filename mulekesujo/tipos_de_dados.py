"""
Tipos de dados básicos

int - Números inteiros
float - Números com casas decimais
string - Tudo que for texto
boolean - Tipo lógico com apenas 2 valores:  True e False
"""

# int

print(10)
print(type(10))

print(30050, type(30050))
print(-30050, type(-30050))

# float

print(10.0, type(10.0))
print(3.14, type(3.14))
print(30500.900, type(30500.900))
print(-30500.900, type(-30500.900))

# string

print("ola gotosa", type("ola gotosa"))
print("11.111.111-ab", type("11.111.111-ab"))
print("print(\"ola gotosa\")", type("print(\"ola gotosa\""))

print("oi \ntudo bem?") #pula 1 linha \n
print("oi \ttudo bem?") #da tab ou da o espaço do tab \t
print("oi \rtudo bem?") #ele apaga oq vem antes \r
print("oi \b tudo bem?") #ele da uma ESPAÇO \b

# boolean (bool)

print()
print(True, type(True))
print(False, type(False))

